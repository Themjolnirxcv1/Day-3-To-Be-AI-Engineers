# aritmetic operators

# +	    Addition	    x + y	
# -	    Subtraction	    x - y	
# *	    Multiplication	x * y	
# /	    Division	    x / y	
# %	    Modulus	        x % y	
# **	Exponentiation	x ** y	
# //	Floor division	x // y


x = 15
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)
print(x // y)

# division di python 
# / operator membagi dua angka dan menghasilkan nilai float
# // operator membagi dua angka dan menghasilkan nilai integer (pembulatan ke bawah)
x = 15
y = 2

print(x / y)   # Output: 7.5