# Operator adalah simbol khusus di Python
# yang digunakan untuk melakukan operasi pada data

# Contoh operator aritmatika:
# +  -> penjumlahan
# -  -> pengurangan
# *  -> perkalian
# /  -> pembagian

# Contoh penggunaan:
# 5 + 3 menghasilkan 8

# Python Operators digunakan untuk memproses nilai dan variabel
# Operator + digunakan untuk menjumlahkan dua nilai
# Operator ini juga bisa dipakai untuk menggabungkan string (concatenation)

# contoh
print(10 + 5)          # Output: 15

# bisa juga digubakan untuk menambahkan gabungan variabel dan value, 
# atua dua variabel

sum1 = 100 + 50     
print(sum1)           # Output: 150
sum2 = sum1 + 250 
print(sum2)           # Output: 400
sum3= sum2 + sum2 
print(sum3)           # Output: 800

# ===== PYTHON OPERATORS =====

# 1. Arithmetic Operators
# Digunakan untuk operasi matematika dasar
# +  penjumlahan
# -  pengurangan
# *  perkalian
# /  pembagian
# %  modulus (sisa bagi)
# ** pangkat
# // pembagian bulat (tanpa koma)

# 2. Assignment Operators
# Digunakan untuk memberi nilai ke variabel
# =   memberi nilai
# +=  menambah lalu menyimpan
# -=  mengurangi lalu menyimpan
# *=  mengalikan lalu menyimpan
# /=  membagi lalu menyimpan

# 3. Comparison Operators
# Digunakan untuk membandingkan dua nilai
# Hasilnya selalu Boolean: True atau False
# ==  sama dengan
# !=  tidak sama dengan
# >   lebih besar
# <   lebih kecil
# >=  lebih besar atau sama dengan
# <=  lebih kecil atau sama dengan

# 4. Logical Operators
# Digunakan untuk menggabungkan kondisi
# and  True jika semua kondisi True
# or   True jika salah satu kondisi True
# not  membalik nilai Boolean

# 5. Identity Operators
# Digunakan untuk membandingkan IDENTITAS objek (bukan nilainya)
# is      True jika dua variabel menunjuk ke objek yang sama
# is not  True jika objeknya berbeda

# 6. Membership Operators
# Digunakan untuk mengecek apakah suatu nilai ada di dalam data
# in      True jika nilai ditemukan
# not in  True jika nilai tidak ditemukan

# 7. Bitwise Operators
# Digunakan untuk operasi level bit (0 dan 1)
# &   AND
# |   OR
# ^   XOR
# ~   NOT
# <<  shift kiri
# >>  shift kanan
